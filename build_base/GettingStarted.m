%% Getting Started with regularizeNd
%% Description
% regularizeNd toolbox is a set of tools to create a gridded lookup table of 
% scattered data in n dimensions. 
%% Features
%% 
% * |regularizeNd| function calculates a gridded lookup table from scattered 
% data in n dimensions.
% * |regularizeNdMatrices| function returns the matrices used to in calculating 
% a gridded lookup table from scattered data. This is useful for solving with 
% constraints.
% * |lsqConstrainedAlternative| is an alternative to |lsqlin| for solving linear 
% least square problems with inequality constraints.
